// curley bracket is used to declare objects

let pen = {
    type: 'ballpoint',
    color: 'black',
    cost: 10
};

/* accessing property
objectName.propertyName
objectName['propertyName']
*/
console.log(pen.cost);
console.log(pen['cost']);

